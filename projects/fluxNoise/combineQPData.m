function [] = plotFit(gamma, A0)
    f = 10.^[-1:0.01:3.5]; 
    A = gamma/4*A0;
    theoryplot = gamma*A./(4*pi^2*f.^2+gamma^2/4);
    figure(113); hold on; 
    plot(f,theoryplot, 'DisplayName', ['fit ',num2str(1e3/gamma),'ms'])
end